# x = int(input("Введите число для поиска: "))

n = 5

array = [1, 2, 3, 4, 5]
x = 6

result = [(array[i] - x) for i in range(n)]

print(result)
print(f"Самое близкое число к {x} в массиве {array} - число {min(result, key=abs) + x}")